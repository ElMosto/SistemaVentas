    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Nombre=txtNombre.getText();
        String Telefono=txtTelefono.getText();
        String Apellido=txtApellido.getText();
        String correo=txtCorreo.getText();
        String Direccion=txtDireccion.getText();
        String Password=txtPassword.getText();
        String ConfirmPassowrd=txtConfirmPassword.getText();
        int rol =0;
        boolean bandera=true;
        
        
        if(Nombre.equals("") || Apellido.equals("") || Password.equals("")){
            JOptionPane.showMessageDialog(null, "Los campos Nombre, Apellido y Password no pueden ir vacios");
            bandera=false;
        }
   
        if(!Password.equals(ConfirmPassowrd)){
            JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden");
            bandera=false;
        }
        
        if(rdoAdmin.isSelected())rol=1;
        if(rdoCaja.isSelected())rol=2;
        
        if(rol==0){
            JOptionPane.showMessageDialog(null, "Selecciona un rol para el usuario");
            bandera=false;
        }
        
        if (bandera){
            UsuarioService us1 = new UsuarioService();
            Usuario u = new Usuario();
            
            u.setNombreUsuario(Nombre);
            u.setApellidoUsuario(Apellido);
            u.setCorreoUsuario(correo);
            u.setTelefonoUsuario(Telefono);
            u.setDireccionUsuario(Direccion);
            u.setIdRolUsuario(rol);
            u.setPasswordUsuario(Password);
            
            
            if(us1.registrarNuevoUsuario(u)){
                JOptionPane.showMessageDialog(null, "Se registro El usuario correctamente");
                borrarDatos();
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

