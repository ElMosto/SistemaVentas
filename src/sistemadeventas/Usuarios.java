    // 1. Para llenar la tabla (Lo llamas al abrir la ventana o al escribir en el buscador)
   public void actualizarTabla(String textoBuscador) {
    UsuarioService m1 = new UsuarioService();
    // 1. Obtienes el modelo de tu tabla real (cambia jTable1 por tu variable)
    DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
    
    // 2. Limpias la tabla
    modelo.setRowCount(0); 

    // 3. Traes los datos de tu servicio
    List<Usuario> lista = m1.obtenerListaUsuarios(textoBuscador);
    
    // 4. Llenas la tabla
    for (Usuario u : lista) {
        modelo.addRow(new Object[]{ u.getNoUsuario(), u.getNombreUsuario() + " " + u.getApellidoUsuario() });
    }
    
    // 5. ¡AQUÍ ES EL MOMENTO SEGURO PARA OCULTAR LA COLUMNA!
    // Porque a este punto, la tabla ya tiene datos y la columna 0 existe.
    jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
    jTable1.getColumnModel().getColumn(0).setMinWidth(0);
    jTable1.getColumnModel().getColumn(0).setPreferredWidth(0);
}

    // 2. Evento MouseClicked de tu miJTable
    private void miJTableMouseClicked() {
        int filaSeleccionada = jTable1.getSelectedRow();
        if (filaSeleccionada != -1) {
            // Recuperamos el ID de la columna 0 (aunque esté oculta)
            int idUsuario = (int) jTable1.getValueAt(filaSeleccionada, 0);
            System.out.println("El ID del usuario seleccionado es: " + idUsuario);
        }
    }
    

